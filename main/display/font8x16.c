const uint8_t* Font8x16_Get(char c)
{
    // Normalize common whitespace
    if (c == ' ' || c == '\t' || c == '\r' || c == '\n') return kSpace;

    // Digits
    if (c >= '0' && c <= '9') return kDigits[(int)(c - '0')];

    // Uppercase
    if (c >= 'A' && c <= 'Z') return kLettersAZ[(int)(c - 'A')];

    // Lowercase -> uppercase (optional)
    if (c >= 'a' && c <= 'z') return kLettersAZ[(int)(c - 'a')];

    // Punctuation (make sure these symbols exist in your file)
    switch (c) {
        case '-': return kDash;
        case '.': return kDot;
        case ':': return kColon;

        case '"': return kQuote;
        case '\'': return kApost;

        case ',': return kComma;
        case ';': return kSemicolon;

        case '/': return kSlash;
        case '\\': return kBackslash;  // IMPORTANT: '\\' not '\'

        case '_': return kUnderscore;
        case '+': return kPlus;
        case '=': return kEqual;

        case '(': return kLParen;
        case ')': return kRParen;

        case '[': return kLBrack;
        case ']': return kRBrack;

        case '<': return kLT;
        case '>': return kGT;

        case '@': return kAt;

        case '!': return kExclam;
        case '?': return kQuestion;

        default:  return kUnknown;
    }
}